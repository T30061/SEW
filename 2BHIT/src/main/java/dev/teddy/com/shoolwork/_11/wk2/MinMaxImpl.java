package dev.teddy.com.shoolwork._11.wk2;

public class MinMaxImpl implements MinMax {

    @Override
    public int min3(int a, int b, int c) {
        int min = a;
        if (b < min) min = b;
        if (c < min) min = c;
        return min;
    }

    @Override
    public int max3(int a, int b, int c) {
        int max = a;
        if (b > max) max = b;
        if (c > max) max = c;
        return max;
    }
}
